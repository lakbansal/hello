package com.sel;

public class Loops {
	public static void main(String [] args)
	{
      int i=2;
		for (int j=2;j<=20;j=j+2)
		{
			System.out.println(j);
			
		}
		while(i<=10)
		
			{
				System.out.println(i);
			    i=i*3;	
			}
		
		
		for (int k=1;k<=10;k++)
		{
			System.out.println(k*5);
		}
		
	}

}
